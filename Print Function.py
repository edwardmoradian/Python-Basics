# an example of end

print("alpha", end =", ")
print("beta", end =", ")
print("epsilon")

print("  *  ")
print(" *** ")
print("*****")

# an example of sep

print("dog","cat","rat")
print("dog","cat","rat", sep="@")

# escape characters: \n new line character

print("hello\n","world")
print("hello\n","world", sep="")

# escape characters: \t go to the next tab
print("hello\tworld")


